# 加密功能：文本转摩斯码
def t2m(text):
    words = text.split(' ')
    output = []
    for word in words:
        lst = []
        for code in word:
            # 判断code在字典morse_dict中
            if code in morse_dict:
                # 将code对应的摩斯码添加到lst中
                lst.append(lst)
        output.append(' '.join(morse_dict[code]))
    return '/'.join(output)


